    public void LoadCustomer() {
        try {
            DefaultTableModel tmd = (DefaultTableModel) tblCustomer.getModel();
            tmd.setRowCount(0);
            for (Customer cus : new CustomerController().getCustomer()) {
                tmd.addRow(new Object[]{
                    cus.getCusid(),
                    cus.getName(),
                    cus.getAddress(),
                    cus.getMobile()

                });

            }
        } catch (ClassNotFoundException | SQLException | NullPointerException ex) {
            Logger.getLogger(interfaceForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

