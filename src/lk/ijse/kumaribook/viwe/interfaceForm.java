    private void setTime() {
        new Timer(0, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                Date d1 = new Date();
                SimpleDateFormat format2 = new SimpleDateFormat("HH:mm:ss");
                txtTime.setText(format2.format(d1));
            }
        }).start();

    }

